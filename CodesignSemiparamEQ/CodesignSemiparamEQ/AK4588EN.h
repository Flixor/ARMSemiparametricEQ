#define AK4588EN_PWRMNGMNT_DEFAULT			0x1F
#define AK4588EN_PLL_CTRL_DEFAULT			0x05
#define AK4588EN_DAC_TDM_DEFAULT			0x00
#define AK4588EN_CTRL1_DEFAULT				0x38
#define AK4588EN_CTRL2_DEFAULT				0x01
